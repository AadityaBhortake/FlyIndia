    /**
     * Confirm button event
     * @param evt 
     */
    private void confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmActionPerformed
        // TODO add your handling code here:
        
        int flag = 0;
        for(int i=0;i<n;i++){
            if(pas[i].name.getText().trim().isEmpty() || pas[i].age.getText().trim().isEmpty() || pas[i].gender.getSelection()==null){
                JOptionPane.showMessageDialog(null, "Please fill all the details!");
                return;
            }
            else if(!pas[i].phone.getText().trim().isEmpty() && flag!=1){
                flag = 1;
            }
        }
        if(flag==0){
            JOptionPane.showMessageDialog(null,"Please enter atleast one Phone number!");
            return;
        }
       
        try{
            BufferedReader reader = new BufferedReader(new InputStreamReader(socket.getInputStream()));
            PrintWriter writer = new PrintWriter(socket.getOutputStream(), true);
            /**
             * Send msg to server for booking confirmation
             */
            writer.println("confirm");
            /**
             * Send passenger details to server
             */
            writer.println(n);
            writer.println(f_code_label.getText());
            for(int i=0;i<n;i++){
                writer.println(pas[i].name.getText());
                writer.println(pas[i].age.getText());
                writer.println(pas[i].gender.getSelection().getActionCommand());
                if(pas[i].phone.getText().trim().isEmpty())
                    writer.println("0");
                else
                    writer.println(pas[i].phone.getText());
            }
            writer.println("END");
            /**
             * Receive status msg from server
             */
            String status = reader.readLine();
            if(status.equals("SUCCESS")){
                System.out.println("SUCCESS");
                booked b = new booked(socket);
                b.set_details(f_name_label.getText(),f_code_label.getText(),src_label.getText(),dest_label.getText(),
                        date_label.getText(),dep_label.getText(),arr_label.getText(),n);
                b.setVisible(true);
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(null,"Booking failed. Please try again!");
            }
        }
        catch(IOException e){
            e.printStackTrace();
        }
        
        
         
        
    }//GEN-LAST:event_confirmActionPerformed

