    /**
     * @param airline
     */
    public void enter_details(String airline,String code,String src,String dest,String date,String dep,String arr,int price,int n){
        this.n=n;
        f_name_label.setText(airline);
        f_code_label.setText(code);
        src_label.setText(src);
        dest_label.setText(dest);
        date_label.setText(date);
        dep_label.setText(dep);
        arr_label.setText(arr);
        pas_label.setText("Passengers: "+n); 
        fin_price.setText("â‚¹"+n*price); //Display the final price for all passengers
        add_passenger.removeAll();
        add_passenger.setLayout(new javax.swing.BoxLayout(add_passenger, javax.swing.BoxLayout.Y_AXIS));
        /**
         * array of passenger.java to get passenger details
         */
        pas = new passenger[n]; 
        for(int i=0;i<n;i++){
            pas[i] = new passenger();
            pas[i].pas_label.setText("Passenger "+ (i+1));
            add_passenger.add(pas[i]);
            
        }
        add_passenger.revalidate();
        add_passenger.repaint();
        
        
    }

